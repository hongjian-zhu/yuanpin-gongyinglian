const state = {
  lang: "en",
  selectedProducts: []
};

const selectedProductsText = () => {
  if (!state.selectedProducts.length) {
    return translations[state.lang].inquiryEmpty;
  }
  return state.selectedProducts.join(", ");
};

const syncInquiry = () => {
  const summary = document.querySelector("[data-selected-products]");
  const field = document.querySelector("[data-products-field]");
  const count = document.querySelector("[data-inquiry-count]");
  const text = selectedProductsText();

  if (summary) summary.textContent = text;
  if (field && state.selectedProducts.length) field.value = state.selectedProducts.join(", ");
  if (count) count.textContent = `RFQ ${state.selectedProducts.length}`;

  document.querySelectorAll("[data-add-product]").forEach((button) => {
    const added = state.selectedProducts.includes(button.dataset.addProduct);
    button.classList.toggle("added", added);
    button.textContent = added ? translations[state.lang].addedInquiry : translations[state.lang].addInquiry;
  });
};

const updateLanguage = (lang) => {
  state.lang = lang;
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";

  const langButton = document.querySelector("[data-lang-toggle]");
  if (langButton) langButton.textContent = lang === "en" ? "中文" : "EN";

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[lang][key]) {
      node.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (translations[lang][key]) {
      node.placeholder = translations[lang][key];
    }
  });

  syncInquiry();
};

document.querySelector("[data-lang-toggle]")?.addEventListener("click", () => {
  updateLanguage(state.lang === "en" ? "zh" : "en");
});

document.querySelectorAll("[data-filter]").forEach((button) => {
  button.addEventListener("click", () => {
    const filter = button.dataset.filter;

    document.querySelectorAll("[data-filter]").forEach((item) => {
      item.classList.toggle("active", item === button);
    });

    document.querySelectorAll("[data-product-card]").forEach((card) => {
      const categories = card.dataset.category.split(" ");
      card.classList.toggle("hidden", filter !== "all" && !categories.includes(filter));
    });
  });
});

document.querySelectorAll("[data-add-product]").forEach((button) => {
  button.addEventListener("click", () => {
    const product = button.dataset.addProduct;
    if (!state.selectedProducts.includes(product)) {
      state.selectedProducts.push(product);
    }
    syncInquiry();
    document.querySelector("#contact")?.scrollIntoView({ behavior: "smooth", block: "start" });
  });
});

const year = document.querySelector("[data-year]");
if (year) year.textContent = new Date().getFullYear();

updateLanguage("en");
